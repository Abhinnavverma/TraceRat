"""Prediction results endpoint.

Receives blast radius predictions from the prediction service
and posts them as GitHub PR comments.
"""

from fastapi import APIRouter, HTTPException, status

from app.models import PredictionResult
from app.services.github_client import GitHubClient
from shared.config import get_settings
from shared.logging import get_logger

logger = get_logger("results")

router = APIRouter(tags=["results"])

# In-memory store for demo mode
_results_store: list[dict] = []


def format_pr_comment(result: PredictionResult) -> str:
    """Format a prediction result as a Markdown PR comment.

    Args:
        result: The prediction result to format.

    Returns:
        Formatted Markdown string.
    """
    risk_emoji = {
        "LOW": "🟢",
        "MEDIUM": "🟡",
        "HIGH": "🟠",
        "CRITICAL": "🔴",
    }

    emoji = risk_emoji.get(result.risk_level.value, "⚪")

    lines = [
        f"## {emoji} Blast Radius Analysis",
        "",
        f"**Risk Score:** `{result.risk_score:.2f}` ({result.risk_level.value})",
        "",
        f"**Summary:** {result.explanation}",
        "",
    ]

    if result.affected_components:
        lines.append("### Affected Components")
        lines.append("")
        lines.append("| Component | Impact | Traffic | Relationship |")
        lines.append("|-----------|--------|---------|--------------|")
        for comp in result.affected_components:
            traffic = comp.traffic_volume or "—"
            lines.append(
                f"| {comp.name} | {comp.impact_score:.2f} | {traffic} | {comp.relationship} |"
            )
        lines.append("")

    if result.traffic_impact:
        lines.append(f"**Traffic Impact:** {result.traffic_impact}")
        lines.append("")

    if result.recommendations:
        lines.append("### Recommendations")
        lines.append("")
        for rec in result.recommendations:
            lines.append(f"- {rec}")
        lines.append("")

    if result.similar_prs:
        lines.append("### Similar Historical PRs")
        lines.append("")
        for pr_url in result.similar_prs:
            lines.append(f"- {pr_url}")
        lines.append("")

    lines.append("---")
    lines.append("*Generated by [TraceRat](https://github.com/tracerat) blast radius predictor*")

    return "\n".join(lines)


@router.get(
    "/results",
    summary="List all stored prediction results",
)
async def list_results():
    """Return all prediction results received so far (demo mode)."""
    return _results_store


@router.get(
    "/results/{event_id}",
    summary="Get a specific prediction result by event_id",
)
async def get_result(event_id: str):
    """Return a single prediction result by event_id."""
    for r in _results_store:
        if r.get("event_id") == event_id:
            return r
    raise HTTPException(status_code=404, detail=f"Result {event_id} not found")


@router.post(
    "/results",
    status_code=status.HTTP_200_OK,
    summary="Receive prediction results and post PR comment",
)
async def receive_results(result: PredictionResult):
    """Receive a prediction result and post it as a GitHub PR comment."""
    settings = get_settings()
    comment_body = format_pr_comment(result)

    # Always store the result locally
    _results_store.append(result.model_dump(mode="json"))
    logger.info(
        "Result stored",
        repo=result.repo_full_name,
        pr_number=result.pr_number,
        risk_level=result.risk_level.value,
        risk_score=result.risk_score,
    )

    # Skip GitHub comment if no App ID is configured (demo mode)
    if not settings.github.app_id:
        logger.info("GitHub App not configured — skipping PR comment (demo mode)")
        return {
            "status": "stored",
            "risk_level": result.risk_level.value,
            "risk_score": result.risk_score,
            "comment_posted": False,
        }

    try:
        owner, repo = result.repo_full_name.split("/", 1)
    except ValueError:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"Invalid repo_full_name: {result.repo_full_name}",
        )

    client = GitHubClient(settings.github)

    try:
        comment_url = await client.post_pr_comment(
            owner=owner,
            repo=repo,
            pr_number=result.pr_number,
            body=comment_body,
            installation_id=result.installation_id,
        )
        logger.info(
            "PR comment posted",
            repo=result.repo_full_name,
            pr_number=result.pr_number,
            risk_level=result.risk_level.value,
            comment_url=comment_url,
        )
        return {
            "status": "posted",
            "comment_url": comment_url,
            "risk_level": result.risk_level.value,
            "comment_posted": True,
        }
    except Exception as e:
        logger.error(
            "Failed to post PR comment",
            repo=result.repo_full_name,
            pr_number=result.pr_number,
            error=str(e),
        )
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail=f"Failed to post PR comment: {e}",
        )
